# Pseudocode:
# Declare variables num1, num2, num3, and string1 with given values.
# Convert num1 into an integer.
# Convert num2 into a float.
# Convert num3 into a string.
# Convert string1 into an integer.
# Print out all the variables on separate lines.

# Declare variables
num1 = 99.23
num2 = 23
num3 = 150
string1 = "100"

# Convert variables
# Convert num1 into an integer
num1_int = int(num1)

# Convert num2 into a float
num2_float = float(num2)

# Convert num3 into a string
num3_string = str(num3)

# Convert string1 into an integer
string1_int = int(string1)

# Print out all the variables on separate lines
print("num1 as an integer:", num1_int)
print("num2 as a float:", num2_float)
print("num3 as a string:", num3_string)
print("string1 as an integer:", string1_int)